    this.score = function(p_name)
    {
        var name = p_name + TERMINAL;
        var score = 0;

        if (falseStarts[name.substr(0, 2)] || falseStarts[name.substr(0, 3)])
        {
            return -1;
        }

        if (falseEnds[p_name.substr(-2)] || falseEnds[p_name.substr(-3)])
        {
            return -1;
        }

        // Invalid: 4 consonants in a row
        if (name.search(/[^aeiouy][^aeiouy][^aeiouy][^aeiouy]/i) != -1)
        {
            return -1;
        }

        // Prefer to start with a different letter
        if (name.substr(0, 1) == original.substr(0, 1))
        {
            score -= 3;
        }

        for (var j = 0; j < name.length - (length-1); j++)
        {
            var fragment = name.substr(j, length);

            if (fragments[fragment])
            {
                if (j == 0 && fragmentIsBeginning(fragment))
                {
                    score += 3;
                }
                else if (j == name.length - length && fragmentIsEnding(fragment))
                {
                    score += 2;
                }
                else
                {
                    score += 1;
                }
            }
        }

        return score;
    }
